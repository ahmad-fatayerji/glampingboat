export function normalizeLocale(locale: unknown): Locale {
  return typeof locale === "string" && LOCALES.includes(locale as Locale)
    ? (locale as Locale)
    : "en";
}

export function tAdmin(
  locale: Locale,
  key: AdminKey,
  replacements?: Record<string, string | number>,
) {
  let value = adminDictionaries[locale]?.[key] ?? en[key] ?? key;
  for (const [name, replacement] of Object.entries(replacements ?? {})) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

export function adminDateFormatter(locale: Locale, options: Intl.DateTimeFormatOptions) {
  return new Intl.DateTimeFormat(ADMIN_INTL_LOCALE[locale], options);
}

export function adminMoneyFormatter(locale: Locale) {
  return new Intl.NumberFormat(ADMIN_INTL_LOCALE[locale], {
    style: "currency",
    currency: "EUR",
  });
}

export function reservationStatusLabel(locale: Locale, status: string) {
  const keys: Record<string, AdminKey> = {
    PENDING_PAYMENT: "pendingPayment",
    CONFIRMED: "confirmed",
    CANCELLED: "cancelled",
    EXPIRED: "expired",
    REFUNDED: "refunded",
  };
  return keys[status] ? tAdmin(locale, keys[status]) : status;
}

